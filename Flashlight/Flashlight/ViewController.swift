import UIKit

class ViewController: UIViewController {

    
    @IBAction func turnFlashlightOn(_ sender: Any) {
        view.backgroundColor = .red
    }
    

    @IBAction func turnFlashlightOff(_ sender: Any) {
        view.backgroundColor = .blue
    }
    
}

